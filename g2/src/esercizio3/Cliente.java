public class Cliente {
	
	public int idCliente;
	public String nome;
	public String cognome;
	public String email;
	
	public Cliente (int idCliente, String nome, String cognome, String email) {
		this.idCliente = idCliente;
		this.nome = nome;
		this.cognome = cognome;
		this.email = email;
	}

}
